import { useEffect, useState } from 'react';
import { useNavigate } from 'react-router-dom';
import { supabase } from '@/lib/supabaseClient';
import { useToast } from '@/hooks/use-toast';

interface RequesterRouteGuardProps {
  children: React.ReactNode;
}

const RequesterRouteGuard = ({ children }: RequesterRouteGuardProps) => {
  const [isLoading, setIsLoading] = useState(true);
  const [isAuthenticated, setIsAuthenticated] = useState(false);
  const navigate = useNavigate();
  const { toast } = useToast();

  useEffect(() => {
    checkAuthentication();
  }, []);

  const checkAuthentication = async () => {
    try {
      // Verify session with Supabase first
      const { data: { session }, error: sessionError } = await supabase.auth.getSession();
      
      if (sessionError || !session) {
        navigate('/login?role=requester');
        return;
      }

      // Get user profile from database using the UUID from Supabase Auth
      const { data: userData, error: userError } = await supabase
        .from('users')
        .select('*')
        .eq('id', session.user.id)
        .single();

      if (userError || !userData) {
        navigate('/login?role=requester');
        return;
      }

      // Check if user has requester role (support both old 'role' and new 'roles' array)
      // Handle roles array - Supabase may return it as string or array
      let userRoles: string[] = [];
      if (userData.roles) {
        if (Array.isArray(userData.roles)) {
          userRoles = userData.roles;
        } else if (typeof userData.roles === 'string') {
          // Parse string representation of array
          try {
            userRoles = JSON.parse(userData.roles);
          } catch (e) {
            // If parsing fails, try to extract from string format
            const match = userData.roles.match(/\[(.*?)\]/);
            if (match) {
              userRoles = match[1].split(',').map(r => r.trim().replace(/['"]/g, ''));
            }
          }
        }
      }
      
      // Fallback to old role field if roles array is empty
      if (userRoles.length === 0 && userData.role) {
        userRoles = [userData.role];
      }
      
      // Check if user has requester role
      if (!userRoles.includes('requester')) {
        // User doesn't have requester role
        console.error('User roles:', userRoles, 'User data:', userData);
        navigate('/login?role=requester');
        return;
      }

      // Update localStorage with correct user info
      const userInfoWithRoles = {
        ...userData,
        roles: userRoles,
        role: userRoles[0] || userData.role || 'requester'
      };
      localStorage.setItem('user_info', JSON.stringify(userInfoWithRoles));
      localStorage.setItem('user_role', 'requester'); // Store selected role
      localStorage.setItem('user_roles', JSON.stringify(userRoles)); // Store all roles
      localStorage.setItem('user_token', session.access_token);
      localStorage.setItem('user_token_data', JSON.stringify({
        token: session.access_token,
        timestamp: Date.now()
      }));

      setIsAuthenticated(true);
    } catch (error) {
      navigate('/login?role=requester');
    } finally {
      setIsLoading(false);
    }
  };

  if (isLoading) {
    return (
      <div className="min-h-screen bg-background flex items-center justify-center">
        <div className="flex items-center gap-2">
          <div className="w-6 h-6 border-2 border-primary border-t-transparent rounded-full animate-spin" />
          <span>Verifying authentication...</span>
        </div>
      </div>
    );
  }

  if (!isAuthenticated) {
    return null;
  }

  return <>{children}</>;
};

export default RequesterRouteGuard;
