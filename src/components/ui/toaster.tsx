import { useToast } from "@/hooks/use-toast"
import {
  Toast,
  ToastClose,
  ToastDescription,
  ToastProvider,
  ToastTitle,
  ToastViewport,
} from "@/components/ui/toast"
import { CheckCircle, XCircle, Info, AlertCircle } from "lucide-react"

export function Toaster() {
  const { toasts } = useToast()

  return (
    <ToastProvider>
      {toasts.map(function ({ id, title, description, action, variant, ...props }) {
        const getIcon = () => {
          if (variant === "success") {
            return <CheckCircle className="h-5 w-5 text-green-600 dark:text-green-400 flex-shrink-0" />
          }
          if (variant === "destructive") {
            return <XCircle className="h-5 w-5 text-red-600 dark:text-red-400 flex-shrink-0" />
          }
          return <Info className="h-5 w-5 text-primary flex-shrink-0" />
        }

        return (
          <Toast key={id} variant={variant} {...props}>
            <div className="flex items-start gap-3 w-full">
              <div className="mt-0.5">
                {getIcon()}
              </div>
              <div className="grid gap-1 flex-1">
                {title && <ToastTitle className="font-semibold">{title}</ToastTitle>}
                {description && (
                  <ToastDescription className="text-sm leading-relaxed">{description}</ToastDescription>
                )}
              </div>
            </div>
            {action}
            <ToastClose />
          </Toast>
        )
      })}
      <ToastViewport />
    </ToastProvider>
  )
}
